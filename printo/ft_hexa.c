#include "ft_printf.h"

void	ft_hexa(unsigned long long n, int *j)
{
	if (n >= 16)
	{
		ft_hexa(n / 16, j);
		ft_hexa(n % 16, j);
	}
	else if (n < 10)
		ft_putchar(n + 48, j);
	else
		ft_putchar(n + 87, j);
}
