#include "libft.h"

int	ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (0 == 0)
	{
		if (s[i] == (char)c)
			return (1);
		if (s[i] == '\0')
			return (0);
		i++;
	}
	return (0);
}
